# my name is jermaine today's date is 9/11/2025
# This program shows how to remove whitespace from a name using lstrip(), rstrip(), and strip().

name = "\t\n   Jermaine   \n\t"  # Name with tabs and newlines

print("Original with whitespace:")
print(name)

print("\nUsing lstrip():")
print(name.lstrip())

print("\nUsing rstrip():")
print(name.rstrip())

print("\nUsing strip():")
print(name.strip())
